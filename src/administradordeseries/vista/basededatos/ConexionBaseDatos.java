    private void jButtonConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConectarActionPerformed

        ConexionDataBase cdb = new ConexionDataBase();

        String servidor = jTextServidor.getText();

        String puerto = jTextPuerto.getText();

        String db = jTextDB.getText();

        String user = jTextUser.getText();

        String password = String.valueOf(jPasswordPass.getPassword());

        if (cdb.conectar(servidor, puerto, db, user, password)) {
            JOptionPane.showMessageDialog(this, "Conexi√≥n exitosa!");
            Principal principal = new Principal(this);
            principal.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "No ha sido posible conectarse, vuelva a intentar!");
        }

    }//GEN-LAST:event_jButtonConectarActionPerformed

