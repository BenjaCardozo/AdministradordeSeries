    private void limpiarCampos() {
        jTextDescripcion.setText("");
        jTextEstrellas.setSelectedIndex(0);
        jTextPrecioAlquiler.setText("");
        jTextTitulo.setText("");
        jComboGenero.setSelectedIndex(0);
        checkboxATP.setState(false);
    }

    private boolean validarCampos() {
        if (jTextDescripcion.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese una descripci√≥n", "ADVERTENCIA!", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (jTextEstrellas.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese una calificaci√≥n en estrella", "ADVERTENCIA!", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (jTextPrecioAlquiler.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el precio de alquiler", "ADVERTENCIA!", JOptionPane.WARNING_MESSAGE);
            return false;
        } else if (jTextTitulo.equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese un titulo", "ADVERTENCIA!", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (jDateFechaEstreno == null) {
            JOptionPane.showMessageDialog(null, "Ingrese una fecha", "ADVERTENCIA!", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }


